package chat.model;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;

@Entity
@Table(name = "message")
@NamedQueries({
    @NamedQuery(name = "chat.model.Message.findAll",
            query = "select e from Message e"),
    @NamedQuery(name = "chat.model.Message.findByName",
            query = "select e from Message e "
            + "where e.firstName like :name "
            + "or e.lastName like :name")
})

public class Message {
    /**
     * Entity's unique identifier.
     */
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private long id;
    /**
     * Message first name.
     */
    @Column(name = "first_name")
    private String firstName;
    /**
     * Message last name.
     */
    @Column(name = "last_name")
    private String lastName;
    /**
     * Message position.
     */
    @Column(name = "e_position")
    private String position;
    /**
     * Message phone.
     */
    private String phone;
    /**
     * Message e-mail.
     */
    private String e_mail;
    /**
     * A no-argument constructor.
     */
    public Message() {
    }
    /**
     * A constructor to create Messages. Id is not passed, cause it's
     * auto-generated by RDBMS.
     *
     */
    public Message(String firstName, String lastName, String position, String phone, String e_mail) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.position = position;
        this.phone = phone;
        this.e_mail = e_mail;
    }
    // Auto-generated equald, hashCode, getters and setters.
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getE_mail() {
		return e_mail;
	}
	public void setE_mail(String e_mail) {
		this.e_mail = e_mail;
	} 
  }